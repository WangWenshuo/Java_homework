package first;

public class Prime_n {
	public Prime_n(){
		
	}
	public Prime_n(int n, int sum) {
		super();
		this.n = n;
		this.sum = sum;
	}
	
	private int n;
	private int sum;
	
	public int getN() {
		return n;
	}
	public void setN(int n) {
		this.n = n;
	}
	public int getSum() {
		return sum;
	}
	public void setSum(int sum) {
		this.sum = sum;
	}
}
